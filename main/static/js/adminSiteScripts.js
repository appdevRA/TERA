$(window).load(function() {
    $('#toggle').click(function() {
        if ($('a.link1').hasClass('disabled')) {
            $('a.link1').removeClass('disabled');
        }
        else {
            $('a.link1').addClass('disabled');
        }
    });
 
    $('a.link1').click(function() {
        if ($(this).hasClass('disabled')) {
            return false;
        }
    });
});

